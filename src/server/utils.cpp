#include "Server.hpp"

void Server::rtrim_crlf(std::string &s)
{
    while (!s.empty()) {
        char c = s[s.size() - 1];
        if (c == '\r' || c == '\n')
            s.erase(s.size() - 1);
        else
            break;
    }
}